const month = "jan"

switch (month) {
    case  "jan":
        console.log("January")
        break;
    case  "feb":
        console.log("Feb")
        break;
    case  "april":
        console.log("march")
        break;
    case  4:
        console.log("apr")
        break;
    case  5:
        console.log("may")
        break;
    case  6:
        console.log("jun")
        break;
    case  7:
        console.log("july")
        break;
    default:
        console.log("defalut")
        break;
}